// Получить случайное число от min до max
function randomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

// Получить N уникальных случайных вариаторов
function getRandomVariators(count) {
    if (count >= ALL_VARIATORS.length) {
        return [...ALL_VARIATORS];
    }
    
    const shuffled = [...ALL_VARIATORS];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled.slice(0, count);
}

// Получить вариаторы для уровня эскалации (количество зависит от уровня)
function getVariatorsForEscalationLevel(level) {
    // level от 1 до 10
    let count;
    if (level === 1) count = randomInt(1, 2);
    else if (level === 2) count = 2;
    else if (level === 3) count = randomInt(2, 3);
    else if (level === 4) count = 3;
    else if (level === 5) count = randomInt(3, 4);
    else if (level === 6) count = 4;
    else if (level === 7) count = randomInt(4, 5);
    else if (level === 8) count = 5;
    else if (level === 9) count = randomInt(5, 6);
    else count = randomInt(5, 7);
    
    return getRandomVariators(count);
}

// Для карточного режима - получить случайный вариатор (без проверки повторов, повторы отлавливаются отдельно)
function getRandomSingleVariator() {
    return ALL_VARIATORS[Math.floor(Math.random() * ALL_VARIATORS.length)];
}
